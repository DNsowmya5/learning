package Math;

public class MathMethods {

	public static void main(String args[])
	{
		

    double num1 = 28;    
    double num2 = 4;    
      
    System.out.println("Maximum number of num1 and num2 is: " +Math.max(num1, num2));   
      
    System.out.println("Square root of num2is: " + Math.sqrt(num2));   
    System.out.println("Square root of num1 is: " + Math.sqrt(num1));   

    System.out.println("Power of num1 and num2 is: " + Math.pow(num1, num2));      

    System.out.println("Logarithm of num1 is: " + Math.log(num1));   
    System.out.println("Logarithm of num2 is: " + Math.log(num2));  
      
    System.out.println("log10 of num1 is: " + Math.log10(num1));   
    System.out.println("log10 of num2 is: " + Math.log10(num2));    
      
    System.out.println("log1p of num1 is: " +Math.log1p(num1));    

    System.out.println("exp of num1 is: " +Math.exp(num1));    
      
    System.out.println("expm1 of num1 is: " +Math.expm1(num1));  
}    
}    
